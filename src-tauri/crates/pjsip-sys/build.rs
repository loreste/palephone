/// Find PJSIP library directories (they contain platform-suffixed .a files)
fn find_pjsip_lib_dirs(pj_src_dir: &Path) -> Vec<PathBuf> {
    let subdirs = [
        "pjlib/lib",
        "pjlib-util/lib",
        "pjnath/lib",
        "pjmedia/lib",
        "pjsip/lib",
        "third_party/lib",
    ];

    subdirs
        .iter()
        .map(|sub| pj_src_dir.join(sub))
        .filter(|p| p.exists())
        .collect()
}

/// Find OpenSSL install prefix
fn find_openssl_prefix(target_os: &str) -> Option<String> {
    // Try pkg-config first
    if let Ok(output) = Command::new("pkg-config")
        .args(["--variable=prefix", "openssl"])
        .output()
    {
        if output.status.success() {
            let prefix = String::from_utf8_lossy(&output.stdout).trim().to_string();
            if !prefix.is_empty() {
                return Some(prefix);
            }
        }
    }

    // Platform-specific defaults
    match target_os {
        "macos" => {
            // Homebrew paths
            for path in &[
                "/opt/homebrew/opt/openssl@3",
                "/opt/homebrew/opt/openssl",
                "/usr/local/opt/openssl@3",
                "/usr/local/opt/openssl",
            ] {
                if Path::new(path).exists() {
                    return Some(path.to_string());
                }
            }
        }
        "linux" => {
            if Path::new("/usr/include/openssl").exists() {
                return Some("/usr".to_string());
            }
        }
        _ => {}
    }

    None
}

/// Find Opus install prefix
fn find_lib_prefix(name: &str, target_os: &str) -> Option<String> {
    if let Ok(output) = Command::new("pkg-config")
        .args(["--variable=prefix", name])
        .output()
    {
        if output.status.success() {
            let prefix = String::from_utf8_lossy(&output.stdout).trim().to_string();
            if !prefix.is_empty() {
                return Some(prefix);
            }
        }
    }
    if target_os == "macos" {
        for path in &[
            format!("/opt/homebrew/opt/{name}"),
            format!("/usr/local/opt/{name}"),
        ] {
            if Path::new(path).exists() {
                return Some(path.clone());
            }
        }
    }
    None
}

fn find_opus_prefix(target_os: &str) -> Option<String> {
    // Try pkg-config
    if let Ok(output) = Command::new("pkg-config")
        .args(["--variable=prefix", "opus"])
        .output()
    {
        if output.status.success() {
            let prefix = String::from_utf8_lossy(&output.stdout).trim().to_string();
            if !prefix.is_empty() {
                return Some(prefix);
            }
        }
    }

    // Homebrew paths on macOS
    if target_os == "macos" {
        for path in &["/opt/homebrew/opt/opus", "/usr/local/opt/opus"] {
            if Path::new(path).exists() {
                return Some(path.to_string());
            }
        }
    }

    None
}

/// Detect the platform suffix PJSIP uses for its library names.
/// Looks for `libpj-<suffix>.a` in pjlib/lib since "pj" is unambiguous.
fn detect_platform_suffix(lib_dirs: &[PathBuf]) -> Option<String> {
    for dir in lib_dirs {
        if let Ok(entries) = fs::read_dir(dir) {
            for entry in entries.flatten() {
                let name = entry.file_name().to_string_lossy().to_string();
                // Match libpj-<something>.a but not libpjsip or libpjlib etc.
                if name.starts_with("libpj-") && name.ends_with(".a") {
                    // "libpj-arm-apple-darwin24.6.0.a" -> "arm-apple-darwin24.6.0"
                    let suffix = &name["libpj-".len()..name.len() - ".a".len()];
                    return Some(suffix.to_string());
                }
            }
        }
    }
    None
}
